export function getInitials(name: string) {
  const parts = name.trim().split(/\s+/);
  if (parts.length === 1) return parts[0].charAt(0).toUpperCase();
  return (parts[0].charAt(0) + parts[parts.length - 1].charAt(0)).toUpperCase();
}

export function calcAge(year: number, month: number, day: number, refDate?: Date) {
  const ref = refDate || new Date();
  let age = ref.getFullYear() - year;
  const refMonth = ref.getMonth() + 1;
  const refDay = ref.getDate();
  if (refMonth < month || (refMonth === month && refDay < day)) age--;
  return age;
}

export function formatDate(day: number, month: number, lang: "id" | "en" = "id") {
  const months = lang === "id" ? MONTH_NAMES : EN_MONTH_NAMES;
  return `${day} ${months[month]}`;
}

export function formatFullDate(day: number, month: number, year: number, lang: "id" | "en" = "id") {
  const months = lang === "id" ? MONTH_NAMES : EN_MONTH_NAMES;
  return `${day} ${months[month]} ${year}`;
}

export function getDaysUntil(day: number, month: number, refDate?: Date) {
  const ref = refDate || new Date();
  const refYear = ref.getFullYear();
  const today = new Date(refYear, ref.getMonth(), ref.getDate());
  let birthday = new Date(refYear, month - 1, day);
  if (birthday < today) birthday = new Date(refYear + 1, month - 1, day);
  return Math.round((birthday.getTime() - today.getTime()) / (1000 * 60 * 60 * 24));
}

export function deduplicate(data: Person[]) {
  const seen = new Map();
  return data.filter((p) => {
    const key = p.name.toLowerCase().replace(/\s+/g, "");
    if (seen.has(key)) return false;
    seen.set(key, true);
    return true;
  });
}

export function filterByDate(day: number, month: number) {
  return deduplicate(BIRTHDAY_DATA.filter((p) => p.day === day && p.month === month));
}

export function getUpcoming(limit: number, refDate?: Date) {
  const ref = refDate || new Date();
  const results: Person[] = [];
  const seen = new Set();

  for (let offset = 1; offset <= 365 && results.length < limit; offset++) {
    const d = new Date(ref);
    d.setDate(d.getDate() + offset);
    const matches = filterByDate(d.getDate(), d.getMonth() + 1);
    for (const m of matches) {
      const key = m.name.toLowerCase().replace(/\s+/g, "");
      if (!seen.has(key)) {
        seen.add(key);
        results.push(m);
      }
    }
  }
  return results.slice(0, limit);
}

export function getUniqueCount() {
  return deduplicate(BIRTHDAY_DATA).length;
}

export function getMonthCount(month: number) {
  return deduplicate(BIRTHDAY_DATA.filter((p) => p.month === month)).length;
}

export function getAverageAge() {
  const unique = deduplicate(BIRTHDAY_DATA).filter((p) => p.year > 1990);
  if (unique.length === 0) return 0;
  const total = unique.reduce((sum, p) => sum + calcAge(p.year, p.month, p.day), 0);
  return Math.round(total / unique.length);
}

export function getMonthCounts() {
  const counts: Record<number, number> = {};
  for (let m = 1; m <= 12; m++) counts[m] = getMonthCount(m);
  return counts;
}

